      # Build Regex
      class BuildRegex
        attr_accessor :regex, :string

        def initialize(hash)
          export = ''

          hash.each do |key, _value|
            export = "#{export}|#{key.to_s.sub(/\(\?(?:i-mx|-mix)\:(.*)\)/, '\1')}"
          end

          export = export.gsub(/^\s*\|/, '')
          @string = export
          @regex = Regexp.new(export.to_s, Regexp::IGNORECASE)
        end
      end

